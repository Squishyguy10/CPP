#include <bits/stdc++.h>

using namespace std;
 
int main() {
    double x = 3.4123;

    cout << x << endl; // normal x
    cout << ceil(x) << endl; // rounds up no matter what
    cout << round(x) << endl; // rounds normally
    cout << floor(x) << endl; // round down no matter what

    return 0;
}